public class Calculator {

  public int add(int no1, int no2){
    return no1 + no2;
  }

  public int subtract(int no1, int no2){
    return no1 - no2;
  }

  public int multiply(int no1, int no2){
    return no1 * no2;
  }

  public double divide(double no1, double no2){
    return no1 / no2;
  }
}
